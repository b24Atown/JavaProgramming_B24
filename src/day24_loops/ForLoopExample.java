package day24_loops;

public class ForLoopExample {
    public static void main(String[] args) {

        for (int i = 1;i<=10;i++) {
            System.out.println(i);
        }
        //the int is what is normally used. it keeps track of the amount of iterations. i is the common name that is declared for that variable. we gave i a value of 1 so we can just have it start at 1. then you want a boolene after that to say how many times to run for. we also want to increment our variable and usually post increment. With a for loop, we need the print statement inside the loop. the i variable is only visible inside the scope of the for loop.
    }
}
