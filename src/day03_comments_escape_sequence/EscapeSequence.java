package day03_comments_escape_sequence;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("I love \"java\" programming");   //how to put quotation marks around a word

        System.out.println("I like \'pizza\' to eat");  // how to put single quotation marks

        System.out.println("Tomorrow is Friday. \nIt is a day off"); // how to make 2nd pard of string go to next line

        System.out.println("\tMessage"); //how to tab







    }
}
