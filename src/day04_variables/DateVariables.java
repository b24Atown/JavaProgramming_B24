package day04_variables;

public class DateVariables {

    public static void main(String[] args) {

        int day = 31;
        int month = 7;
        int year = 2021;

        System.out.println("Date: " + month + "/" + day + "/" + year);

        day = 1;
        month = 8;

        System.out.println("Tomorrows date:" + month + "/" + day + "/" + year);






    }


}
