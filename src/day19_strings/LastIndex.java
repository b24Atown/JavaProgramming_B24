package day19_strings;

public class LastIndex {
    public static void main(String[] args) {

        //inidexOf-gives first occurrence of a letter
        //lastIndexOf() gives last occurrence of a letter

        String str = "java";
        System.out.println(str.indexOf("a")); //gives first a position
        System.out.println(str.lastIndexOf("a")); // this will give last index of that letter that were looking for.



    }
}
